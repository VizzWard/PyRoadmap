# Conversión implícita de entero a float
entero = 5
flotante = 2.0
resultado = entero + flotante
print(resultado)  # Salida: 7.0 (Python convierte el entero a float automáticamente)

# Conversión implícita en operaciones aritméticas
a = 5
b = 2
resultado = a / b
print(resultado)  # Salida: 2.5 (Python convierte el resultado a float automáticamente)

# Concatenación implícita de string y número
texto = "Tengo " + str(25) + " años"
print(texto)  # Salida: "Tengo 25 años" (Python convierte el número a string automáticamente)