# Conversión de entero a float
numero_entero = 5
numero_float = float(numero_entero)
print(numero_float)  # Salida: 5.0

# Conversión de float a entero
numero_float = 7.8
numero_entero = int(numero_float)
print(numero_entero)  # Salida: 7

# Conversión de número a string
numero = 10
texto = str(numero)
print(texto)  # Salida: "10"

# Conversión de string a entero
texto = "15"
numero = int(texto)
print(numero)  # Salida: 15