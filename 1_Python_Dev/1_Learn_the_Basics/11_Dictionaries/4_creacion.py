# 3. Creación de Diccionarios
# Ejemplo 1: Usando el constructor dict()
dict_constructor = dict(nombre="María", edad=25, ciudad="Barcelona")
print("Diccionario creado con constructor:", dict_constructor)

# Ejemplo 2: A partir de una lista de tuplas
lista_tuplas = [("fruta", "manzana"), ("color", "rojo"), ("cantidad", 5)]
dict_desde_tuplas = dict(lista_tuplas)
print("Diccionario desde lista de tuplas:", dict_desde_tuplas)