# Ejemplos prácticos de Diccionarios en Python

# 1. Introducción a los Diccionarios
# Ejemplo 1: Creación básica de un diccionario
diccionario_basico = {"nombre": "Juan", "edad": 30, "ciudad": "Madrid"}
print("Diccionario básico:", diccionario_basico)

# Ejemplo 2: Diccionario con diferentes tipos de datos
diccionario_mixto = {
    "entero": 10,
    "flotante": 3.14,
    "booleano": True,
    "lista": [1, 2, 3],
    "tupla": (4, 5, 6)
}
print("Diccionario con tipos mixtos:", diccionario_mixto)









